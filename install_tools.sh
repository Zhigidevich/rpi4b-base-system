#!/bin/bash

# Выход при любой ошибке, незаданной переменной или сбое в пайплайне
set -euo pipefail

# Цвета для вывода текста
GREEN='\033[0;32m'
BLUE='\033[0;34m'
RED='\033[0;31m'
NC='\033[0m' # No Color

echo -e "${BLUE}=== Запуск скрипта настройки Ubuntu 24.04 ===${NC}"

# 1. Проверка на запуск от root (через sudo)
if [ "$EUID" -ne 0 ]; then
    echo -e "${RED}Ошибка: Этот скрипт должен быть запущен с правами sudo.${NC}"
    echo "Пример: sudo ./install_tools.sh"
    exit 1
fi

# 2. Обновление индексов пакетов
echo -e "\n${BLUE}[1/4] Обновление списков пакетов...${NC}"
apt-get update -y

# 3. Список пакетов для установки через APT
# Разделяйте пакеты пробелами. Символ '\' позволяет переносить строки для удобства.
APT_PACKETS=(
    curl
    wget
    git
    tmux
    htop
    net-tools
    build-essential
    jq
)

echo -e "\n${BLUE}[2/4] Установка системных утилит через APT...${NC}"
# Установка пакетов (флаг -y автоматически соглашается с установкой)
apt-get install -y "${APT_PACKETS[@]}"

# 4. Установка пакетов через Snap (если требуются современные/изолированные версии)
echo -e "\n${BLUE}[3/4] Установка пакетов через Snap...${NC}"
# Пример: snap install nmap
# Если не нужно, эту секцию можно удалить

# 5. Очистка системы от ненужного мусора после установки
echo -e "\n${BLUE}[4/4] Очистка кэша и неиспользуемых зависимостей...${NC}"
apt-get autoremove -y
apt-get clean

echo -e "\n${GREEN}=== Установка успешно завершена! ===${NC}"
